package com.nit.car.Model;

public class Card {
	 private	String card_id;
	 private	String card_no;
	 private	String card_oldername;
	 private	String card_expirydate;
	public String getCard_id() {
		return card_id;
	}
	public void setCard_id(String card_id) {
		this.card_id = card_id;
	}
	public String getCard_no() {
		return card_no;
	}
	public void setCard_no(String card_no) {
		this.card_no = card_no;
	}
	public String getCard_oldername() {
		return card_oldername;
	}
	public void setCard_oldername(String card_oldername) {
		this.card_oldername = card_oldername;
	}
	public String getCard_expirydate() {
		return card_expirydate;
	}
	public void setCard_expirydate(String card_expirydate) {
		this.card_expirydate = card_expirydate;
	}
}
